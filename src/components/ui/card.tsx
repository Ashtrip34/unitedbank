import * as React from "react"

import { cn } from "@/lib/utils"

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & { variant?: 'default' | 'glass' | 'elevated' | 'gradient' }
>(({ className, variant = 'default', ...props }, ref) => {
  const variants = {
    default: "rounded-xl border bg-card text-card-foreground shadow-sm",
    glass: "rounded-xl border border-border/50 backdrop-blur-xl bg-card/80 text-card-foreground shadow-lg",
    elevated: "rounded-xl bg-card text-card-foreground shadow-lg hover:shadow-xl transition-shadow duration-300",
    gradient: "rounded-xl bg-gradient-to-br from-card to-muted text-card-foreground shadow-md",
  }
  
  return (
    <div
      ref={ref}
      className={cn(variants[variant], className)}
      {...props}
    />
  )
})
Card.displayName = "Card"

